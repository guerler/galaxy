<script setup>
import FormMessage from "components/Form/FormMessage";
import ToolFooter from "components/Tool/ToolFooter";
import ToolHelp from "components/Tool/ToolHelp";
import ToolFavoriteButton from "components/Tool/Buttons/ToolFavoriteButton.vue";
import ToolVersionsButton from "components/Tool/Buttons/ToolVersionsButton.vue";
import ToolOptionsButton from "components/Tool/Buttons/ToolOptionsButton.vue";

import ariaAlert from "utils/ariaAlert";
import Webhooks from "mvc/webhooks";
import { computed, ref, watch } from "vue";
import { useCurrentUser } from "composables/user";

const props = defineProps({
    id: {
        type: String,
        required: true,
    },
    version: {
        type: String,
        required: false,
    },
    title: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: false,
    },
    options: {
        type: Object,
        required: true,
    },
    messageText: {
        type: String,
        required: true,
    },
    messageVariant: {
        type: String,
        default: "info",
    },
    disabled: {
        type: Boolean,
        default: false,
    },
});

const emit = defineEmits(["onUpdateFavorites"]);

function onUpdateFavorites(user, favorites) {
    emit("onUpdateFavorites", user, favorites);
}

const errorText = ref(null);

watch(
    () => props.id,
    () => {
        errorText.value = null;
    }
);

function onSetError(e) {
    errorText.value = e;
}

const user = useCurrentUser();
const hasUser = computed(() => !user.value.isAnonymous);

const versions = computed(() => props.options.versions);
const showVersions = computed(() => props.options.versions?.length > 1);
</script>

<template>
    <div>
        <div class="sticky-top bg-secondary px-2 py-1 rounded">
            <div class="d-flex justify-content-between">
                <div class="py-1">
                    <icon icon="wrench" class="fa-fw mr-1" />
                    <b itemprop="name">{{ title }}</b>
                    <span itemprop="description">{{ description }}</span> (Galaxy Version {{ version }})
                </div>
                <b-button-group class="tool-card-buttons">
                    <ToolFavoriteButton
                        v-if="hasUser"
                        :id="props.id"
                        @onSetError="onSetError"
                        @onUpdateFavorites="onUpdateFavorites" />
                    <ToolVersionsButton v-if="showVersions" :version="props.version" :versions="versions" />
                    <ToolOptionsButton :id="props.id" :sharableUrl="props.options.sharable_url" />
                </b-button-group>
            </div>
        </div>
    </div>
</template>

<style lang="scss" scoped>
.fa-wrench {
    cursor: unset;
}

.tool-card-buttons {
    height: 2em;
}
</style>
